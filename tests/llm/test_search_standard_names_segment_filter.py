class TestMCPServerSegmentParams:
    """Verify search_standard_names tool in server.py accepts segment params."""

    def test_search_sn_tool_accepts_physical_base(self) -> None:
        """search_standard_names tool must accept physical_base and other segment kwargs."""
        import inspect

        from imas_codex.llm.server import AgentsServer

        server = AgentsServer(dd_only=False, include_standard_names=True)

        # Find the registered search_standard_names tool function via MCP components
        tool_fn = None
        for key, component in server.mcp._local_provider._components.items():
            if key.startswith("tool:") and "search_standard_names" in key:
                tool_fn = component.fn
                break

        assert tool_fn is not None, "search_standard_names tool not registered"
        sig = inspect.signature(tool_fn)
        params = sig.parameters
        assert "physical_base" in params, "physical_base param must be in MCP tool"
        assert "subject" in params, "subject param must be in MCP tool"
        assert "transformation" in params, "transformation param must be in MCP tool"
        assert "component" in params, "component param must be in MCP tool"
        assert "coordinate" in params, "coordinate param must be in MCP tool"
        assert "process" in params, "process param must be in MCP tool"
        assert "position" in params, "position param must be in MCP tool"
        assert "region" in params, "region param must be in MCP tool"
        assert "device" in params, "device param must be in MCP tool"
        assert "geometric_base" in params, "geometric_base param must be in MCP tool"
